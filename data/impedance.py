IMPEDANCE_DATA = {
    "copper": {
        "0.6/1kV": COPPER_IMPEDANCE_1KV,
        "6/10kV":  COPPER_IMPEDANCE_10KV,
    },
    "aluminum": {
        "0.6/1kV": ALUMINUM_IMPEDANCE_1KV,
        "6/10kV":  ALUMINUM_IMPEDANCE_10KV,
    },
}


def get_impedance(conductor: str, voltage_level: str,
                  section: float) -> tuple:
    """
    获取指定条件下的电缆阻抗参数。

    参数:
        conductor: "copper" | "aluminum"
        voltage_level: "0.6/1kV" | "6/10kV"
        section: 截面 (mm²)

    返回:
        (R0, X0): (Ω/km, Ω/km)
    """
    data = IMPEDANCE_DATA[conductor][voltage_level]
    # 找到最近的标准截面
    standard = sorted(data.keys())
    best = min(standard, key=lambda x: abs(x - section))
    return data[best]


def get_r0(conductor: str, voltage_level: str, section: float) -> float:
    """获取单位电阻 R0 (Ω/km)"""
    return get_impedance(conductor, voltage_level, section)[0]


def get_x0(conductor: str, voltage_level: str, section: float) -> float:
    """获取单位电抗 X0 (Ω/km)"""
    return get_impedance(conductor, voltage_level, section)[1]
